'use strict'

const image = document.querySelector('.image');
const overlay = document.querySelector('.overlay');
const btnCloseImage = document.querySelector('.close-image');
const btnsModelImage = document.querySelectorAll('.model-image');
console.log(btnsModelImage);
for (let i = 0; i < btnsModelImage.length; i++)
    btnsModelImage[i].addEventListener('click',function(){
        console.log("button clicked");
        image.classList.remove('hidden');
        overlay.classList.remove('hidden');
    });
btnCloseImage.addEventListener('click',function(){
    image.classList.add('hidden');
    overlay.classList.add('hidden');
});

